function renderCategoryTree($categories) {
    foreach ($categories as $cat) {
        $hasChildren = !empty($cat['children']);
        echo '<div class="category-item">';
        echo '<div class="category-header" data-slug="' . htmlspecialchars($cat['slug']) . '">';
        echo '<div class="category-info">';
        echo '<div class="category-icon">' . htmlspecialchars($cat['icon']) . '</div>';
        echo '<div class="category-name">' . htmlspecialchars($cat['name']) . '</div>';
        echo '</div>';
        if ($hasChildren) echo '<span class="category-toggle">▼</span>';
        echo '</div>';
        if ($hasChildren) {
            echo '<div class="subcategories" data-slug="' . htmlspecialchars($cat['slug']) . '">';
            foreach ($cat['children'] as $subKey => $subValue) {
                if (is_array($subValue)) {
                    // Это раздел с подразделами
                    echo '<div class="subcategory-group">';
                    echo '<div class="subcategory-group-title">' . htmlspecialchars($subKey) . '</div>';
                    echo '<div class="subcategory-list">';
                    foreach ($subValue as $item) {
                        $url = '/category/' . $cat['slug'] . '/' . urlencode($subKey) . '/' . urlencode($item);
                        echo '<a href="' . htmlspecialchars($url) . '" class="subcategory-link">' . htmlspecialchars($item) . '</a>';
                    }
                    echo '</div>';
                    echo '</div>';
                } else {
                    // Обычная подкатегория (без разделов)
                    $url = '/category/' . $cat['slug'] . '/' . urlencode($subValue);
                    echo '<a href="' . htmlspecialchars($url) . '" class="subcategory-link">' . htmlspecialchars($subValue) . '</a>';
                }
            }
            echo '</div>';
        }
        echo '</div>';
    }
}
